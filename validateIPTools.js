
/**
 * Validate IPv4
 */
function ipv4(address) {
  var reg = /^(25[0-5]|2[0-4][0-9]|1[0-9][0-9]|[0-9]{1,2})(\.(25[0-5]|2[0-4][0-9]|1[0-9][0-9]|[0-9]{1,2})){3}$/;
  return reg.test(address);
}

/**
 * Validate ipv4 and mask address;
 * If required = true, the mask must be contained
 * @format: 0.0.0.0/0
 */
function ipv4WithMask (address, required) {
  var reg = /^(25[0-5]|2[0-4][0-9]|1[0-9][0-9]|[0-9]{1,2})(\.(25[0-5]|2[0-4][0-9]|1[0-9][0-9]|[0-9]{1,2})){3}$/;
  if (address.indexOf('/') != -1) {
    var addressList = address.split('/');
    var ip = addressList[0];
    var len = parseInt(addressList[1]);
    return addressList.length == 2 && (len >= 0 && len <= 32) && reg.test(ip);
  }

  return required ? false : reg.test(address);
}

